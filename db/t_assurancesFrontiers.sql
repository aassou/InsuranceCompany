	dateEffet DATE DEFAULT NULL,
	duree INT(12) DEFAULT NULL,
	dateExpiration DATE DEFAULT NULL,
	proprietaire VARCHAR(100) DEFAULT NULL,
	passeport VARCHAR(50) DEFAULT NULL,
	cin VARCHAR(50) DEFAULT NULL,
